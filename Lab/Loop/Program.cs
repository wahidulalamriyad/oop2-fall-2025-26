// for loop
for (int i = 0; i < 5; i++)
{
    Console.WriteLine($"For loop iteration: {i}");
}

// while loop
int count = 0;
while (count < 5)
{
    Console.WriteLine($"While loop count: {count}");
    count++;
}

// do-while loop
int doCount = 0;
do
{
    Console.WriteLine($"Do-While loop count: {doCount}");
    doCount++;
} while (doCount < 5);
